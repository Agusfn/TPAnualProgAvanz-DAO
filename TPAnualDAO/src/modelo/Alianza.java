package modelo;

public class Alianza {

	private int id;
	private String nombre;
	
	public Alianza() 
	{
	}	
	public Alianza(int id)
	{
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
	
}
